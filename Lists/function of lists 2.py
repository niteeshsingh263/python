a =["Ironman","Thor","Captain America","Hulk","Ironman"]

# to create a copy of a list

b = a.copy()
print(b)

# to access an element
b = a.index("Hulk")
print(b)

# to entend the list
c = [" Niteesh" ,"Abhay"]
a.extend(c)
print(a)

# to reverse the list

a.reverse()
print(a)

# to sort the list
a.sort()
print(a)

# to clear all the data from the list
a.clear()
print(a)